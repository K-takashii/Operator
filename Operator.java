class Operator {
    public static void main(String[] args) {
        int x = 10;
        int y = 2;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y); //掛け算
        System.out.println(x / y); //割り算
        System.out.println(x % y); //剰余(割り算の余り)
    }
}